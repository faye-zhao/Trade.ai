import SwiftUI

struct ContentView: View {
    @State private var selectedTab = 0
    
    var body: some View {
        TabView(selection: $selectedTab) {
            HomeView()
                .tabItem {
                    Image(systemName: selectedTab == 0 ? "house.fill" : "house")
                    Text("Home")
                }
                .tag(0)
                        
            NotificationsView()
                .tabItem {
                    Image(systemName: selectedTab == 1 ? "bell.fill" : "bell")
                    Text("Notifications")
                }
                .tag(1)
        }
    }
}

